@RunWith(SpringRunner.class)
@ActiveProfiles({"local", "componenttest"})
@SpringBootTest(webEnvironment = SpringBootTest.WebEnvironment.MOCK)
public class ReferenceDataControllerTest {

    @MockBean
    private SiteService<Site, String> siteService;

    @Autowired
    private WebApplicationContext webApplicationContext;

    @Autowired
    private ServiceResolverBackdoor serviceRequestAuthorizer;

    @Autowired
    private UserResolverBackdoor userRequestAuthorizer;

    @Autowired
    private ObjectMapper objectMapper;

    private static final String USER_ID = UserResolverBackdoor.CITIZEN_ID;

    private RestActions restActions;

    MockMvc mockMvc;

    @Before
    public void setUp(){
        mockMvc = webAppContextSetup(webApplicationContext).apply(springSecurity()).build();
        List<Site> serviceReturn = Arrays.asList(Site.siteWith()
                .sopReference("sop")
                .siteId("AA99")
                .name("name")
                .service("service")
                .id(1)
                .build(),
            Site.siteWith()
                .sopReference("sop")
                .siteId("AA001")
                .name("name")
                .service("service")
                .id(1)
                .build()
        );

        when(siteService.getAllSites()).thenReturn(serviceReturn);
    }


    @Test
    public void shouldReturnReferenceDataSites() throws Exception {
        MvcResult mvcResult = this.mockMvc.perform(get("/reference-data/sites"))
                                        .andExpect(status().isOk())
                                        .andReturn();
        List<SiteDTO> actualResponse = objectMapper.readValue(mvcResult.getResponse().getContentAsString(),List.class);
        assertEquals(2,actualResponse.size());
    }
}
